<b>基本操作</b>
<ul>
鼠标拖动，滚轮放大缩小地图，点击空白旋转地图</br>
*中国和美国可以放大到county（县）级地图</br>
*地图数据仅供学习参考使用
</ul>
</br>
<b>1、创建年代</b>
<ul>
未知年代》输入年份</br>
</ul>
</br>
<b>2、创建王朝</b>
<ul>
未知王朝》输入王朝信息</br>
</ul>
</br>
<b>3、设置都城</b>
<ul>
放大地图》选择区域》确定都城</br>
*都城可以设置在state（州）和county（县）级</br>
</ul>
</br>
<b>4、王朝版图</b>
<ul>
当前王朝》选择区域》添加该区域》删除该区域</br>
*当前王朝用红色区域表示</br>
</ul>
</br>
<b>5、修改王朝</b>
<ul>
选择当前王朝的首都》修改王朝信息</br>
*取消都城后可以重新定都</br>
</ul>


